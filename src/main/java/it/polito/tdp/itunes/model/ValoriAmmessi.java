package it.polito.tdp.itunes.model;

public class ValoriAmmessi {

	private int min;
	private int max;
	
	public ValoriAmmessi(int min, int max) {
		super();
		this.min = min;
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public int getMax() {
		return max;
	}
	
	
}
